<script lang="ts">
  import type { SelectOption } from "src/definitions/form";

  import { createEventDispatcher } from "svelte";

  const dispatch = createEventDispatcher<{ search: SelectOption }>();

  export let name: string;
  export let id = name;
  export let options: Array<SelectOption> = [];
  export let placeholder = "";

  let searchInput: HTMLInputElement;

  const listName = `${name}-list`;

  let selectedOption: SelectOption | undefined;

  const handleInput = (e: any) => {
    const foundOption = options.filter((item) =>
      item.label.match(e.target.value)
    );

    if (foundOption.length > 0) {
      selectedOption = foundOption[0];
    }

    if (foundOption.length === 1) {
      dispatch("search", selectedOption);
      searchInput.value = "";
      selectedOption = undefined;
    }
  };

  const handleKeypress = (e: KeyboardEvent) => {
    if (e.key === "Enter") {
      e.preventDefault();
    }

    if (selectedOption && e.key === "Enter") {
      dispatch("search", selectedOption);
      searchInput.value = "";
      selectedOption = undefined;
    }
  };
</script>

<div class="fr-input-group">
  <p class="fr-hint-text fr-my-2w">
    <span class="fr-fi-information-line" aria-hidden="true" />
    Appuyez sur <kbd>Enter</kbd> pour ajouter votre mot-cl√©
  </p>
  <input
    role="search"
    autocomplete="off"
    class="fr-input"
    {placeholder}
    list={listName}
    {id}
    {name}
    on:keypress={handleKeypress}
    bind:this={searchInput}
    on:input|stopPropagation|preventDefault={handleInput}
  />

  <datalist id={listName}>
    {#each options as { label }}
      <option value={label} />
    {/each}
  </datalist>
</div>

